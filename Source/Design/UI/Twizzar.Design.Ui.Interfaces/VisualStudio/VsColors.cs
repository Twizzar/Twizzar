namespace Twizzar.Design.Ui.Interfaces.VisualStudio
{
    /// <summary>
    /// Enum for mapping to vs colors.
    /// </summary>
    public enum VsColors
    {
        /// <summary>
        /// Defined background color.
        /// </summary>
        BackgroundColor,

        /// <summary>
        /// Background brush.
        /// </summary>
        BackgroundBrush,

        /// <summary>
        /// 2nd Background brush.
        /// </summary>
        BackgroundSecondaryBrush,

        /// <summary>
        /// Text brush.
        /// </summary>
        TextBrush,

        /// <summary>
        /// 2nd text brush.
        /// </summary>
        TextSecondaryBrush,

        /// <summary>
        /// 3rd text brush.
        /// </summary>
        TextTertiaryBrush,

        /// <summary>
        /// Caret brush.
        /// </summary>
        CaretBrush,

        /// <summary>
        /// Inactive text brush.
        /// </summary>
        InactiveTextBrush,

        /// <summary>
        /// Border brush.
        /// </summary>
        BorderBrush,

        /// <summary>
        /// Glyph brush.
        /// </summary>
        GlyphBrushKey,

        /// <summary>
        /// Glyph expanded brush.
        /// </summary>
        GlyphExpandedBrush,

        /// <summary>
        /// Glyph mouse over brush.
        /// </summary>
        GlyphMouseOverBrush,

        /// <summary>
        /// Glyph collapsed brush.
        /// </summary>
        GlyphCollapsedBrush,
    }
}
